//NCAAF schedule api integration
export const getAllNcaafSchedulelist = async (division, year, week, onSuccess, onFailure) => {
  try {
    const res = await webApi.post(`/getNcaaScheduleList`, {
      league: division ? division : "",
      year: year ? year : "",
      week: week ? week : "",
    });
    if (res.status === 200) {
      const r = res.data;
      return onSuccess(r);
    } else {
      onFailure?.("Something Wrong! Please Try again later" + res.data);
    }
  } catch (error) {
    onFailure?.("Something Wrong! Please Try again later" + error);
  }
};

export const getAllNcaafSchedulelistById = async (year, teamId, onSuccess, onFailure) => {
  try {
    const res = await webApi.post(`/getNcaaScheduleList`, {
      year: year ? year : "",
      team: teamId ? teamId : "",
    });
    if (res.status === 200) {
      const r = res.data;

      return onSuccess(r);
    } else {
      onFailure?.("Something Wrong! Please Try again later" + res.data);
    }
  } catch (error) {
    onFailure?.("Something Wrong! Please Try again later" + error);
  }
};

